package petle_i_pliki;

public class WP_13 {
    public static void main(String[] args) {

        int k = 7;
        for (int i = 0; i < 7; i++) {
            System.out.println();
            for (int j = k; j > 0; j--) {
                System.out.print("#");
            }
            k--;
        }
    }
}
